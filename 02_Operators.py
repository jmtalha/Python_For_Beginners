# Python has a concept known as Operators 
# Addition, Substraction, Multiplication, Division, Modulus, Exponentiation, Floor Division

a = 10
b = 20

print(a+b)      #Addition   
print(a-b)      #Substraction
print(a*b)      #Multiplication
print(a/b)      #Division
print(a%b)      #Modulus
print(a**b)     #Exponentiation
print(a//b)     #Floor Division
